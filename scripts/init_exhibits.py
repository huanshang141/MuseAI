def generate_exhibits():
    """Generate exhibit data with detailed content."""
    exhibits = []

    for category, items in EXHIBIT_DETAILS.items():
        for item in items:
            # 随机选择楼层和对应展厅
            floor = random.choice([1, 2, 3])
            hall = random.choice(HALLS_BY_FLOOR[floor])

            # 根据类别选择合理的年代（使用实际年代）
            era = item.get("era", "清代")

            # 重要性 1-5（按内容丰富度）
            importance = min(5, len(item["content"]) // 200 + 1)

            # 预计参观时间 5-30分钟（按内容长度）
            estimated_visit_time = min(30, max(5, len(item["content"]) // 100))

            # 随机坐标（模拟展厅内位置）
            location_x = round(random.uniform(10, 90), 2)
            location_y = round(random.uniform(10, 90), 2)

            exhibits.append({
                "name": item["name"],
                "description": item["content"][:100] + "...",  # 简短描述
                "content": item["content"],  # 详细内容用于 RAG
                "category": category,
                "hall": hall,
                "floor": floor,
                "era": era,
                "importance": importance,
                "estimated_visit_time": estimated_visit_time,
                "location_x": location_x,
                "location_y": location_y,
            })

    return exhibits


SAMPLE_EXHIBITS = generate_exhibits()


